import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __dir = dirname(fileURLToPath(import.meta.url));
const raw = JSON.parse(readFileSync(join(__dir, 'ontologicalAtlas.json'), 'utf8'));

const out = `// Auto-generated by scripts/generate-ontological-atlas.mjs — do not edit
// Ontological Atlas data: ${raw.schools.length} schools, ${raw.works.length} works, ${raw.personas.length} personas, ${raw.dilemmas.length} dilemmas
// Crawled: ${raw.crawledAt}

export const OA_SCHOOLS = ${JSON.stringify(raw.schools)};
export const OA_WORKS = ${JSON.stringify(raw.works)};
export const OA_PERSONAS = ${JSON.stringify(raw.personas)};
export const OA_DILEMMAS = ${JSON.stringify(raw.dilemmas)};
export const OA_CRAWL_DATE = '${raw.crawledAt}';
`;

writeFileSync(join(__dir, '../src/constants/ontologicalAtlas.js'), out, 'utf8');
const size = (out.length / 1024 / 1024).toFixed(2);
console.log(`Written src/constants/ontologicalAtlas.js (${size} MB)`);
console.log(`  ${raw.schools.length} schools, ${raw.works.length} works, ${raw.personas.length} personas, ${raw.dilemmas.length} dilemmas`);
