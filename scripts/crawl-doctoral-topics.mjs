// Run: node scripts/crawl-doctoral-topics.mjs
// Outputs: src/constants/doctoralTopics.js

import { writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = join(__dirname, '../src/constants/doctoralTopics.js');

const BASE = 'https://phd.nthrys.com';
const SITEMAP_COUNT = 13;

async function fetchSitemap(n) {
  const url = `${BASE}/sitemap/${String(n).padStart(4, '0')}.xml`;
  console.log(`  Fetching sitemap ${n}...`);
  const res = await fetch(url);
  if (!res.ok) throw new Error(`Sitemap ${n}: HTTP ${res.status}`);
  return res.text();
}

function parseLevel2(xml) {
  const result = {};
  const re = /<loc>https:\/\/phd\.nthrys\.com\/([^/<]+)\/([^/<\s]+)<\/loc>/g;
  let m;
  while ((m = re.exec(xml)) !== null) {
    const subject = m[1];
    const topic = m[2];
    if (!result[subject]) result[subject] = new Set();
    result[subject].add(topic);
  }
  return result;
}

async function main() {
  console.log('Crawling phd.nthrys.com sitemaps...\n');

  const indices = Array.from({ length: SITEMAP_COUNT }, (_, i) => i + 1);
  const results = await Promise.allSettled(indices.map(fetchSitemap));

  const merged = {};
  let failCount = 0;

  for (let i = 0; i < results.length; i++) {
    const r = results[i];
    if (r.status === 'rejected') {
      console.warn(`  Sitemap ${i + 1} failed: ${r.reason.message}`);
      failCount++;
      continue;
    }
    const parsed = parseLevel2(r.value);
    for (const [sub, topics] of Object.entries(parsed)) {
      if (!merged[sub]) merged[sub] = new Set();
      for (const t of topics) merged[sub].add(t);
    }
  }

  const sortedSubjects = Object.keys(merged).sort();
  const topicsBySubject = {};
  let totalTopics = 0;

  for (const sub of sortedSubjects) {
    topicsBySubject[sub] = [...merged[sub]].sort();
    totalTopics += topicsBySubject[sub].length;
  }

  console.log(`\nResults:`);
  console.log(`  Subjects: ${sortedSubjects.length}`);
  console.log(`  Topics:   ${totalTopics.toLocaleString()}`);
  console.log(`  Sitemaps failed: ${failCount}/${SITEMAP_COUNT}`);

  const output = `// Auto-generated by scripts/crawl-doctoral-topics.mjs
// Last crawled: ${new Date().toISOString()}
// Subjects: ${sortedSubjects.length} | Topics: ${totalTopics.toLocaleString()}

export const DOCTORAL_SUBJECTS = ${JSON.stringify(sortedSubjects, null, 2)};

export const DOCTORAL_TOPICS = ${JSON.stringify(topicsBySubject, null, 2)};

export const DOCTORAL_TOTAL = ${totalTopics};
`;

  writeFileSync(OUT, output, 'utf8');
  console.log(`\nWrote ${OUT}`);
}

main().catch(e => { console.error(e); process.exit(1); });
